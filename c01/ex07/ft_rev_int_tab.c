void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	swap;

	i = 0;
	while (i < size - 1)
	{
		swap = tab[size - 1 ];
		tab[size - 1] = tab[i];
		tab[i] = swap;
		i++;
		size--;
	}
}
