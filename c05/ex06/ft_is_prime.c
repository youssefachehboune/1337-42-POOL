int	ft_is_prime(int nb)
{
	int	m;
	int	i;

	i = 2;
	if (nb == 0 || nb == 1 || nb < 0)
	{
		return (0);
	}
	while (i < nb)
	{
		m = nb % i;
		if (m == 0)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
