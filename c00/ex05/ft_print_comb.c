#include <unistd.h>

void	ft_print_comb(void)
{
	int	i;
	int	j;
	int	z;

	i = '0';
	while (i <= '7')
	{
		j = i;
		while (++j <= '8')
		{
			z = j;
			while (++z <= '9')
			{
				write(1, &i, 1);
				write(1, &j, 1);
				write(1, &z, 1);
				if (i != '7' || j != '8' || z != '9')
				{
					write(1, ", ", 2);
				}
			}
		}
		i++;
	}
}
